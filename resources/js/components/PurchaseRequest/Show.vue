        <div class="row mb-1">
            <div class="col-6 text-muted">Requester / អ្នកស្នើសុំ:</div>
            <div class="col-6 font-weight-bold">{{ purchaseRequest.creator_name ?? 'N/A' }}</div>
        </div>
        <div class="row mb-1">
            <div class="col-6 text-muted">ID Card / អត្តលេខ:</div>
